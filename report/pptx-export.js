    function downloadReportPptx(_themeName) {
        const btn = document.getElementById('pptx-export-btn');
        if (btn) { btn.disabled = true; btn.dataset._origText = btn.textContent; btn.textContent = 'Building…'; }

        function reset() {
            if (btn) {
                btn.disabled = false;
                btn.textContent = btn.dataset._origText || 'Download PPTX';
                delete btn.dataset._origText;
            }
        }

        try {
            const meta = scrapeMeta();
            const blocks = scrapeBlocks();
            const reportState = (typeof window.__odinGetReportState === 'function')
                ? window.__odinGetReportState()
                : null;
            const isDisagg = !!(reportState && reportState.architecture === 'disaggregated');

            const logoPromise = fetchLogoBase64();
            const plan = [Promise.resolve(buildCoverSlide(meta))];
            SECTION_PLAN.forEach(function(entry) {
                plan.push(buildSectionSlide(entry, blocks, isDisagg));
            });
            plan.push(Promise.resolve(buildClosingSlide()));

            Promise.all([Promise.all(plan), logoPromise]).then(function(parts) {
                const slides = parts[0].filter(Boolean);
                const logoPng = parts[1];
                // Renumber image targets so each slide's media file has a unique name.
                let imgIdx = 0;
                slides.forEach(function(sl) {
                    (sl.images || []).forEach(function(img) {
                        imgIdx += 1;
                        img.target = 'image' + imgIdx + '.png';
                    });
                });
                return buildPackage(slides, logoPng);
            }).then(function(blob) {
                const name = 'odin-configuration-report_' + nowStamp() + '.pptx';
                triggerDownload(blob, name);
            }).catch(function(err) {
                console.error('PPTX export failed:', err);
                alert('PPTX export failed: ' + (err && err.message ? err.message : err));
            }).then(reset);
        } catch (err) {
            console.error('PPTX export failed:', err);
            alert('PPTX export failed: ' + (err && err.message ? err.message : err));
            reset();
        }
    }
