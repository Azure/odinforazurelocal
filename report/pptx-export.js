    function buildPackage(slides, logoBase64) {
        if (!window.JSZip) {
            throw new Error('JSZip is not available. Ensure pptxgen.bundle.js is loaded.');
        }
        const zip = new window.JSZip();

        zip.file('[Content_Types].xml', buildContentTypesXml(slides.length));
        zip.file('_rels/.rels', RELS_ROOT_XML);
        zip.file('ppt/presProps.xml', PRES_PROPS_XML);
        zip.file('ppt/viewProps.xml', VIEW_PROPS_XML);
        zip.file('ppt/theme/theme1.xml', THEME1_XML);
        zip.file('ppt/slideMasters/slideMaster1.xml', SLIDE_MASTER1_XML);
        zip.file('ppt/slideMasters/_rels/slideMaster1.xml.rels', SLIDE_MASTER1_RELS_XML);
        zip.file('ppt/slideLayouts/slideLayout1.xml', SLIDE_LAYOUT1_XML);
        zip.file('ppt/slideLayouts/_rels/slideLayout1.xml.rels', SLIDE_LAYOUT1_RELS_XML);
        zip.file('ppt/presentation.xml', buildPresentationXml(slides.length));
        zip.file('ppt/_rels/presentation.xml.rels', buildPresentationRelsXml(slides.length));

        slides.forEach(function(sl, i) {
            const n = i + 1;
            zip.file('ppt/slides/slide' + n + '.xml', sl.xml);
            zip.file('ppt/slides/_rels/slide' + n + '.xml.rels',
                buildSlideRels(sl.images, sl.links));
            (sl.images || []).forEach(function(img) {
                if (img.png) zip.file('ppt/media/' + img.target, img.png, { base64: true });
            });
        });

        if (logoBase64) {
            zip.file('ppt/media/odin-logo.png', logoBase64, { base64: true });
        }

        return zip.generateAsync({
            type: 'blob',
            mimeType: 'application/vnd.openxmlformats-officedocument.presentationml.presentation',
            compression: 'DEFLATE'
        });
    }

    function triggerDownload(blob, filename) {
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = filename;
        document.body.appendChild(a);
        a.click();
        setTimeout(function() {
            document.body.removeChild(a);
            URL.revokeObjectURL(url);
        }, 0);
    }

