    // Returns a list of bullet strings describing NIC topology: total ports,
    // speed mix, RDMA-enabled count. Works for both architectures.
    function buildNicSummary(s) {
        if (!s) return [];
        const out = [];

        if (s.architecture === 'disaggregated') {
            const pc = s.disaggPortConfig || {};
            const keys = Object.keys(pc).filter(function(k) {
                // BMC port doesn't count as a data NIC.
                return pc[k] && k.indexOf('bmc') !== 0;
            });
            if (!keys.length) {
                // Fall back to default NIC slots when ports haven't been
                // individually configured.
                const ports = parseInt(s.disaggPortCount, 10) || 0;
                if (ports) out.push('NICs per node: ' + ports + ' data ports + 1 BMC');
                return out;
            }
            out.push('NICs per node: ' + keys.length + ' data ports + 1 BMC');

            // CodeQL js/remote-property-injection (#17): use a Map so
            // user-controlled NIC speed strings are stored as explicit
            // key/value pairs with safe dictionary semantics.
            const disaggBySpeed = new Map();
            let rdmaCount = 0;
            keys.forEach(function(k) {
                const sp = (pc[k].speed) || (s.disaggPortSpeeds && s.disaggPortSpeeds[k.split('_')[0]]) || 'unknown';
                disaggBySpeed.set(sp, (disaggBySpeed.get(sp) || 0) + 1);
                if (pc[k].rdma === true) rdmaCount += 1;
            });
            const speedParts = Array.from(disaggBySpeed.entries()).map(function(entry) {
                return entry[1] + '× ' + entry[0];
            });
            if (speedParts.length) out.push('Port speeds: ' + speedParts.join(', '));
            out.push('RDMA-capable ports: ' + rdmaCount + ' / ' + keys.length);
            return out;
        }

        // Hyperconverged
        const cfg = Array.isArray(s.portConfig) ? s.portConfig : [];
        if (!cfg.length) {
            const nports = parseInt(s.ports, 10) || 0;
            if (nports) out.push('NICs per node: ' + nports);
            return out;
        }
        out.push('NICs per node: ' + cfg.length);
        // CodeQL js/remote-property-injection (#16): use a Map so
        // user-controlled NIC speed strings are stored as explicit
        // key/value pairs with safe dictionary semantics.
        const hciBySpeed = new Map();
        let rdma = 0;
        cfg.forEach(function(p) {
            const sp = (p && p.speed) || 'unknown';
            hciBySpeed.set(sp, (hciBySpeed.get(sp) || 0) + 1);
            if (p && p.rdma === true) rdma += 1;
        });
        const sp2 = Array.from(hciBySpeed.entries()).map(function(entry) { return entry[1] + '× ' + entry[0]; });
        if (sp2.length) out.push('Port speeds: ' + sp2.join(', '));
        out.push('RDMA-capable ports: ' + rdma + ' / ' + cfg.length);
        return out;
    }

