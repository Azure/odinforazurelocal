    function ipToInt(ip) {
        const o = String(ip || '').split('.').map(Number);
        if (o.length !== 4 || o.some(function(n) { return isNaN(n); })) return null;
        return ((o[0] << 24) >>> 0) + (o[1] << 16) + (o[2] << 8) + o[3];
    }

